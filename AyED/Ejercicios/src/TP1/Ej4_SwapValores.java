package TP1;

public class Ej4_SwapValores {
    public static void swap1 (int x, int y) {
        if (x < y) {
            int tmp = x;
            x = y;
            y = tmp;
        }
    }
    public static void swap2 (Integer x, Integer y) {
        if (x < y) {
            int tmp = x;
            x = y;
            y = tmp;
        }
    }
}
